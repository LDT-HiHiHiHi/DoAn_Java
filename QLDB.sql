CREATE TABLE LOGIN(
	ID NCHAR(12) NOT NULL,
	USERNAME NVARCHAR(50),
	EMAIL NCHAR(50),
	SEX NVARCHAR(20),
	PASSWORD NCHAR(25),
	CONSTRAINT PK_LOGIN PRIMARY KEY (ID)
)
CREATE TABLE THONGTIN_NGUOIDUNG(
	ID NCHAR(12) NOT NULL,
	EMAIL NCHAR(50),
	SEX NVARCHAR(20),
	CONSTRAINT PK_THONGTIN_NGUOIDUNG PRIMARY KEY (ID),
	CONSTRAINT FK_THONGTIN_NGUOIDUNG_LOGIN FOREIGN KEY (ID) REFERENCES LOGIN(ID) 
)

CREATE TABLE THONGTINLIENHE(
	SDT NCHAR(12) NOT NULL,
	NAME NVARCHAR(50),
	SEX NVARCHAR(20),
	NOTED NVARCHAR(50),
	GROUPNAME NVARCHAR(50), -- CÓ THỂ LÀ BẠN BÈ, ĐỒNG NGHIỆP, NULL
	CONSTRAINT PK_THONGTINLIENHE PRIMARY KEY (SDT)
)

CREATE TABLE LIENHE(
	ID NCHAR(12) NOT NULL,
	SDT NCHAR(12),
	NGAYLUU DATE,
	CONSTRAINT PK_LIENHE PRIMARY KEY (ID,SDT),
	CONSTRAINT FK_LIENHE_LOGIN FOREIGN KEY (ID) REFERENCES LOGIN(ID),
	CONSTRAINT FK_LIENHE_THONGTINLIENHE FOREIGN KEY (SDT) REFERENCES THONGTINLIENHE(SDT)
)	

CREATE TABLE ND_SAOLUU(
	MASAOLUU NCHAR(12) NOT NULL,
	TENSAOLUU NVARCHAR(40),
	SDT NCHAR(12),
	NAME NVARCHAR(50),
	CONSTRAINT PK_ND_SAOLUU PRIMARY KEY (MASAOLUU),
	CONSTRAINT FK_ND_SAOLUU_THONGTINLIENHE FOREIGN KEY (SDT) REFERENCES THONGTINLIENHE(SDT) 
)

CREATE TABLE SAOLUU(
	ID NCHAR(12) NOT NULL,
	MASAOLUU NCHAR(12) NOT NULL,
	NGAYTAO DATE,
	CONSTRAINT PK_SAOLUU PRIMARY KEY (ID,MASAOLUU),
	CONSTRAINT FK_SAOLUU_LOGIN FOREIGN KEY (ID) REFERENCES LOGIN(ID),
	CONSTRAINT FK_SAOLUU_ND_SAOLUU FOREIGN KEY (MASAOLUU) REFERENCES ND_SAOLUU(MASAOLUU) 
)

